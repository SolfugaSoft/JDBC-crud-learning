package com.solfugasoft.JDBC_CRUD_APP.models;

public class Authorities {

    private int id;
    private String role;

    public Authorities() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
